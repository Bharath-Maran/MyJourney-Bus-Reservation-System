CREATE TABLE TRAVEL_DETAILS(
TRAVEL_ID INT PRIMARY KEY AUTO_INCREMENT,
BUS_ID INT,
OPERATOR_NAME VARCHAR(50),
SOURCE_LOCATION VARCHAR(50),
DESTINATION_LOCATION VARCHAR(50),
SOURCE_DATE DATE,
DESTINATION_DATE DATE,
SOURCE_TIME TIME,
DESTINATION_TIME TIME,
TRAVEL_DISTANCE INT,
TOTAL_FARE INT,
TOTAL_SEATS INT,
AVAILABLE_SEATS INT,
BOOKED_SEATS INT,
TEMPORARY_SEATS INT,
FOREIGN KEY (BUS_ID) REFERENCES BUS_DETAILS(BUS_ID));
